#include <unistd.h>
#include "ft_printf.h"

void	ft_putstr(const char *c)
{
	int		i;

	if (c == NULL)
		write(1, "(null)", 6);
	else
	{
		i = 0;
		while (c[i] != '\0')
			i++;
		write(1, c, i);
	}
}
